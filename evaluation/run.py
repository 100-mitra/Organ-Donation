"""Run the whole evaluation: comparison + sensitivity + systems -> RESULTS.md + charts.

  python -m evaluation.run        (or: python evaluation/run.py)

Deterministic for the fixed config below, so the repo reproduces clean.
"""
from __future__ import annotations

import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from evaluation import charts
from evaluation.metrics import compare, to_markdown
from evaluation.policies import cas_policy, rank_cas, rank_fcfs
from evaluation.sensitivity import run_sweeps
from evaluation.simulate import simulate
from evaluation.systems import measure
from evaluation.waitlist import generate

N_REC, N_DON, SEED, LONG_W = 400, 150, 0, 25

# On-chain gas/op — illustrative, measured deterministically by
# `cd contracts && npx hardhat run scripts/gas.js`.
GAS = {
    "registerRecipient": 90716, "registerDonor": 46178,
    "logDecision (pool=5)": 72391, "eraseRecipient": 29017,
}


def _pct(x):
    return f"{x * 100:.0f}%"


def bullets(comp):
    f, c = comp["FCFS"], comp["CAS"]
    long_name = next(k for k in comp if k.startswith("CAS+longevity"))
    lo = comp[long_name]
    return [
        f"**High-CPRA (sensitized) access**: FCFS {_pct(f['rate_high_cpra'])} → CAS {_pct(c['rate_high_cpra'])} — "
        f"CAS prioritizes hard-to-match patients (cost: low-CPRA {_pct(f['rate_low_cpra'])} → {_pct(c['rate_low_cpra'])}).",
        f"**Pediatric access**: FCFS {_pct(f['rate_pediatric'])} → CAS {_pct(c['rate_pediatric'])} → +longevity "
        f"{_pct(lo['rate_pediatric'])} (cost: adult access falls {_pct(f['rate_adult'])} → {_pct(c['rate_adult'])}).",
        f"**Blood-type-O disadvantage**: under CAS, O {_pct(c['rate_O'])} vs non-O {_pct(c['rate_nonO'])} — it PERSISTS "
        f"(an ABO-compatibility effect, not fixed by scoring; present under FCFS too: O {_pct(f['rate_O'])} vs {_pct(f['rate_nonO'])}).",
        f"**Waiting time at transplant**: FCFS median {f['waiting_median']:.0f}d (pure queue) vs CAS "
        f"{c['waiting_median']:.0f}d — CAS trades strict seniority for the weighted score.",
        f"**Longevity tension**: turning the longevity weight on drops mean transplant age {c['age_mean']:.0f} → "
        f"{lo['age_mean']:.0f} — maximizing life-years disadvantages older patients (so it is weight-0 by default).",
    ]


def results_md(comp, sweeps, sysm):
    L = []
    L.append("# Evaluation results — Phase 5 (metrics)\n")
    L.append("> Generated by `python -m evaluation.run`. **Framing:** these describe **what the mechanism "
             "does** — the trade-offs each policy makes — NOT a proof of real-world fairness, which "
             "synthetic data cannot establish. The defensible signal is the *relative* shift CAS makes vs "
             "the FCFS baseline.\n")
    L.append(f"**Config:** {N_REC} recipients, {N_DON} deceased donors, seed {SEED}, longevity weight (on) = {LONG_W}. "
             "Synthetic India ABO/HLA/CPRA distributions (engine/data_gen.py).\n")
    L.append("## Equity: FCFS vs CAS vs CAS+longevity\n")
    L.append(to_markdown(comp) + "\n")
    L.append("### What the numbers say\n")
    L += [f"- {b}" for b in bullets(comp)]
    L.append("\n![subgroup access](out/equity_access.png)\n")
    L.append("![waiting time](out/waiting_hist.png)\n")
    L.append("## Sensitivity — the weights are tunable dials\n")
    for label, data in sweeps.items():
        pts = data["points"]
        L.append(f"- **{label}**: {pts[0][1]:.2f} (w={pts[0][0]}) → {pts[-1][1]:.2f} (w={pts[-1][0]}).")
    L.append("\n![sensitivity](out/sensitivity.png)\n")
    L.append("## Systems metrics (illustrative — one machine, single-process Python)\n")
    L.append(f"- throughput: **{sysm['throughput_allocs_per_s']} allocations/s** "
             f"({sysm['allocations']} allocations in {sysm['sim_seconds']}s)")
    L.append(f"- single-match latency: **{sysm['single_match_ms']} ms** to rank a {sysm['waitlist_size']}-recipient waitlist\n")
    L.append("On-chain gas/op (illustrative; `cd contracts && npx hardhat run scripts/gas.js`):\n")
    L.append("| op | gas |\n|---|---|")
    for k, v in GAS.items():
        L.append(f"| {k} | {v:,} |")
    L.append("\n## Reproduce\n```\npip install -r requirements.txt\npython -m evaluation.run\n```")
    return "\n".join(L) + "\n"


def main():
    w, d = generate(N_REC, N_DON, SEED)
    comp = compare(w, d, LONG_W)
    sweeps = run_sweeps(w, d)
    sysm = measure(w, d)
    cas_waits = [t["waiting_days"] for t in simulate(w, d, rank_cas, cas_policy())["transplants"]]
    fcfs_waits = [t["waiting_days"] for t in simulate(w, d, rank_fcfs, cas_policy())["transplants"]]

    out = Path(__file__).resolve().parent / "out"
    out.mkdir(exist_ok=True)
    charts.equity_access(comp, out / "equity_access.png")
    charts.waiting_hist(cas_waits, fcfs_waits, out / "waiting_hist.png")
    charts.sensitivity_grid(sweeps, out / "sensitivity.png")

    (Path(__file__).resolve().parent / "RESULTS.md").write_text(
        results_md(comp, sweeps, sysm), encoding="utf-8", newline="\n"
    )
    print(f"wrote evaluation/RESULTS.md + 3 charts to {out}")


if __name__ == "__main__":
    main()
