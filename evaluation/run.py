"""Run the whole evaluation over MANY seeds -> RESULTS.md (mean ± std) + charts.

  python -m evaluation.run        (or: python evaluation/run.py)

Equity + sensitivity aggregate over SEEDS so the headline is not a single-seed
artifact; systems timing is illustrative (one seed, machine-dependent).
"""
from __future__ import annotations

import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from evaluation import charts
from evaluation.aggregate import (
    aggregate_comparison, aggregate_sensitivity, bullets, comparison_md, denominators_md,
)
from evaluation.policies import cas_policy, rank_cas, rank_fcfs
from evaluation.simulate import simulate
from evaluation.systems import measure
from evaluation.waitlist import generate

N_REC, N_DON, LONG_W = 400, 150, 25
SEEDS = list(range(30))

GAS = {
    "registerRecipient": 90716, "registerDonor": 46178,
    "logDecision (pool=5)": 72391, "eraseRecipient": 29017,
}


def results_md(agg, sweeps, sysm):
    n = len(SEEDS)
    L = ["# Evaluation results — Phase 5 (metrics)\n"]
    L.append(f"> Generated by `python -m evaluation.run` over **{n} seeds**. **Framing:** these describe "
             "**what the mechanism does** — the relative trade-offs each policy makes — NOT a proof of "
             "real-world fairness, which synthetic data cannot establish. **± is the standard deviation "
             "ACROSS seeds** (seed-to-seed spread); the 95% CI of the mean is ~√n tighter (≈ ÷5.5 here).\n")
    L.append(f"**Config:** {N_REC} recipients, {N_DON} deceased donors, {n} seeds (0–{n - 1}), longevity "
             f"weight (on) = {LONG_W}. Synthetic India ABO/HLA/CPRA distributions (engine/data_gen.py).\n")
    L.append("## Equity: FCFS vs CAS vs CAS+longevity\n")
    L.append(comparison_md(agg) + "\n")
    L.append("### Subgroup denominators — what each % is *out of*\n")
    L.append(denominators_md(agg, N_REC) + "\n")
    L.append("### What the numbers say\n")
    L += [f"- {b}" for b in bullets(agg)]
    L.append("\n![subgroup access](out/equity_access.png)\n")
    L.append("![waiting time](out/waiting_hist.png)\n")
    L.append("## Sensitivity — the weights are tunable dials (mean ± std over seeds)\n")
    for label, data in sweeps.items():
        a, b = data["points"][0], data["points"][-1]
        L.append(f"- **{label}**: {a[1]['mean']:.2f} ± {a[1]['std']:.2f} (w={a[0]}) → "
                 f"{b[1]['mean']:.2f} ± {b[1]['std']:.2f} (w={b[0]}).")
    L.append("\n![sensitivity](out/sensitivity.png)\n")
    L.append("## Systems metrics (illustrative — one machine, single-process Python, one seed)\n")
    L.append(f"- throughput: **{sysm['throughput_allocs_per_s']} allocations/s** "
             f"({sysm['allocations']} allocations in {sysm['sim_seconds']}s)")
    L.append(f"- single-match latency: **{sysm['single_match_ms']} ms** to rank a {sysm['waitlist_size']}-recipient waitlist\n")
    L.append("On-chain gas/op (illustrative; `cd contracts && npx hardhat run scripts/gas.js`):\n")
    L.append("| op | gas |\n|---|---|")
    L += [f"| {k} | {v:,} |" for k, v in GAS.items()]
    L.append("\n## Reproduce\n```\npip install -r requirements.txt\npython -m evaluation.run\n```")
    return "\n".join(L) + "\n"


def main():
    agg = aggregate_comparison(SEEDS, N_REC, N_DON, LONG_W)
    sweeps = aggregate_sensitivity(SEEDS, N_REC, N_DON)
    sysm = measure(*generate(N_REC, N_DON, seed=0))

    cas_waits, fcfs_waits = [], []
    for s in SEEDS[:8]:  # pool a few seeds for the waiting-time distribution
        w, d = generate(N_REC, N_DON, seed=s)
        cas_waits += [t["waiting_days"] for t in simulate(w, d, rank_cas, cas_policy())["transplants"]]
        fcfs_waits += [t["waiting_days"] for t in simulate(w, d, rank_fcfs, cas_policy())["transplants"]]

    out = Path(__file__).resolve().parent / "out"
    out.mkdir(exist_ok=True)
    charts.equity_access(agg, out / "equity_access.png")
    charts.waiting_hist(cas_waits, fcfs_waits, out / "waiting_hist.png")
    charts.sensitivity_grid(sweeps, out / "sensitivity.png")

    (Path(__file__).resolve().parent / "RESULTS.md").write_text(
        results_md(agg, sweeps, sysm), encoding="utf-8", newline="\n"
    )
    print(f"wrote evaluation/RESULTS.md + 3 charts over {len(SEEDS)} seeds")


if __name__ == "__main__":
    main()
